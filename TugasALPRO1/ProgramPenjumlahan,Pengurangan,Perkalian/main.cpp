#include<iostream>
using namespace std;

int main()
{
    int y,Q;
    cout <<"PROGRAM PENJUMLAHAN, PENGURANGAN, PERKALIAN 2 BILANGAN" << endl;
    cout <<"Masukan Nilai y :";cin >>y;
    cout <<"Masukan Nilai Q :";cin >>Q;
    cout <<"Penjumlahan :"<<(y+Q)<<endl;
    cout <<"Pengurangan :"<<(y-Q)<<endl;
    cout <<"Perkalian :"<<(y*Q)<<endl;
    return 0;

}
